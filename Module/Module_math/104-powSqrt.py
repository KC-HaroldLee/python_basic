# 지수와 로그
import math

# math.pow(x,y), sqrt()
print(math.pow(2,10)) # 1024.0
print(math.sqrt(1024)) # 32.0

# math.exp(x)
# ex결과를 반환
print('e^1 =', math.exp(1)) # e^1 = 2.718281828459045
print('e^2 =', math.exp(2)) # e^2 = 7.38905609893065

# math.log()
print(math.log(2)) # 0.6931471805599453
print(math.log(math.e)) # 1.0

